    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(isEmptyFild() == false){
            validador validador = new validador();
            validador.setNome(txtName.getText());
            validador.setCPF(txtCPF.getText());
            validador.setTelefone(txtFone.getText());
            validador.setData(txtData.getText());
            validador.setCliente(CheckClient.isSelected());
            
            try {
                if(validador.save() == true){
                    JOptionPane.showMessageDialog(null, "Cadastro salvo");      
                    TelaInicial.tableModel.adicionar();
                }
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null,"Informe um data valida ");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

