    boolean isEmptyFild(){
        
        boolean empty = true;
        if(txtName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        }else if(txtCPF.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        }else if(txtFone.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        }else if(txtData.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        }
        else{
            empty = false;
        }
        return empty;
        
    }
